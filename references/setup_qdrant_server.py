# ── interactive prompts ─────────────────────────────────────────────────────

def ask(msg, default):
    val = input(f"  {msg} [{default}]: ").strip()
    return val or default


# ── main ─────────────────────────────────────────────────────────────────────

def parse_args():
    p = argparse.ArgumentParser(
        description="Qdrant server setup — install on a fresh VM (binary or Docker).",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""Examples:
  sudo python3 setup_qdrant_server.py              # Interactive binary install
  sudo python3 setup_qdrant_server.py --docker    # Docker install
  sudo python3 setup_qdrant_server.py --cloud aws # AWS deployment guide
  sudo python3 setup_qdrant_server.py --non-interactive --version v1.8.0
        """
    )
    p.add_argument("--docker", action="store_true", help="Use Docker instead of binary")
    p.add_argument("--cloud", choices=["digitalocean", "hetzner", "aws"],
                   help="Print cloud provider deployment guide")
    p.add_argument("--version", default="v1.7.4", help="Qdrant version (default: v1.7.4)")
    p.add_argument("--bind", default="0.0.0.0", help="Bind address (default: 0.0.0.0)")
    p.add_argument("--port", type=int, default=6333, help="gRPC port (default: 6333)")
    p.add_argument("--max-disk-space-gb", type=int, default=0,
                   help="Max disk space per collection in GB (0 = unlimited)")
    p.add_argument("--non-interactive", action="store_true",
                   help="Use all defaults, skip all prompts")
    return p.parse_args()


def main():
    args = parse_args()
    print("=" * 60)
    print("  Qdrant Server Setup")
    print("=" * 60)

    if args.cloud:
        {
            "digitalocean": print_digitalocean_instructions,
            "hetzner": print_hetzner_instructions,
            "aws": print_aws_instructions,
        }[args.cloud]()
        return

    check_root()

    if not args.non_interactive:
        mode = input("\n  Mode: [binary] / docker / cloud: ").strip().lower()
        if mode == "docker":
            args.docker = True
        elif mode == "cloud":
            print("  Providers: digitalocean, hetzner, aws")
            provider = input("  Provider: ").strip().lower()
            {
                "digitalocean": print_digitalocean_instructions,
                "hetzner": print_hetzner_instructions,
                "aws": print_aws_instructions,
            }.get(provider, lambda: None)()
            return

    if not args.docker:
        if not args.non_interactive:
            args.bind = ask("Bind address", "0.0.0.0")
            args.port = int(ask("gRPC port", "6333"))
            args.max_disk_space_gb = int(ask("Max disk (GB, 0=unlimited)", "0")) or None
            args.version = ask("Qdrant version", "v1.7.4")
        install_binary(args)
    else:
        install_docker(args)

    # ── health check ────────────────────────────────────────────────────
    rest_port = args.port + 1
    bind_host = args.bind if args.bind != "0.0.0.0" else "localhost"
    print(f"\n[*] Waiting for Qdrant to respond on http://{bind_host}:{rest_port} …")
    if wait_for_qdrant(bind_host, rest_port):
        ok("Qdrant is healthy and responding.")
    else:
        warn("Qdrant did not respond in 30s — run: systemctl status qdrant")

    print("\n" + "=" * 60)
    print(f"  {GREEN}Setup complete!{RESET}")
    print("=" * 60)
    bind_for_export = args.bind if args.bind != "0.0.0.0" else "<server-ip>"
    print(f"\n  On your Hermes bot server(s), set:")
    print(f"    export QDRANT_HOST={bind_for_export}")
    print(f"    export BOT_ID=your-bot-name")
    print(f"    python ~/.hermes/skills/memory/vector-memory/references/setup_once.py")
    print()


if __name__ == "__main__":
    main()
